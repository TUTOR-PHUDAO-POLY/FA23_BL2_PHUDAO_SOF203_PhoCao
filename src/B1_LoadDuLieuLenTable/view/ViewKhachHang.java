    public void showDataTable(ArrayList<KhachHang> lists) {
        dtm.setRowCount(0);
        for (KhachHang kh : lists) {
            dtm.addRow(new Object[]{
                kh.getMa(), kh.getTen(), kh.getTuoi(),
                kh.isGioiTinh(), kh.getLoai()
            });
        }
    }

